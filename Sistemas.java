package scl.edu.gt;

public class Sistemas {
	public static void Bios() {
		System.out.println(" El propósito fundamental de la BIOS es "
				+ "\n iniciar, probar el hardware del sistema y "
				+ "\n cargar un gestor de arranque o un sistema operativo "
				+ "\n desde de un dispositivo de almacenamiento de datos.");
	}
	public static void Kernel() {
		System.out.println(" En informática, un núcleo o kernel es un software "
				+ "\n que constituye una parte fundamental del sistema operativo,"
				+ "\n y se define como la parte que se ejecuta en modo privilegiado.");
	}
	public static void CPU() {
		System.out.println(" Su trabajo es interpretar las instrucciones "
				+ "\n de un programa informático mediante la realización de"
				+ "\n las operaciones básicas aritméticas, lógicas y externas.");
	}
	public static void Hardward() {
		System.out.println(" El hardware, equipo o soporte físico​ en informática se "
				+ "\n refiere a las partes físicas, tangibles, de un sistema informático, "
				+ "\n sus componentes eléctricos, electrónicos, electromecánicos y mecánicos.​");
	}
	public static void Software() {
		System.out.println(" Se conoce como software​, logicial o soporte lógico al sistema "
				+ "\n formal de un sistema informático, que comprende el conjunto de los componentes "
				+ "\n lógicos necesarios que hacen posible la realización de tareas específicas, en "
				+ "\n contraposición a los componentes físicos que son llamados hardware.");
	}

}
